# Solicita entrada dos usuários
number_1 = float(input('Digite o primeiro número: '))
number_2 = float(input('Digite o segundo número: '))

# Realiza operações
soma = number_1 + number_2
subtracao = number_1 - number_2
multiplicacao = number_1 * number_2
divisao = number_1 / number_2

# Exibe resultados
print(f"Soma: {soma}")
print(f"Subtração: {subtracao}")
print(f"Multiplicação: {multiplicacao}")
print(f"Divisão: {divisao}")